<p> <b>Responsive web design </b>is a web development approach that creates dynamic changes to the appearance of a website, depending on the screen size and orientation of the device being used to view it.</p>
<p>A mobile app is a software application developed specifically for use on small, wireless computing devices, such as smartphones, and tablets, rather than desktop or laptop computers. </p>
<p><b>Advantage of Responsive web design</b></p>
<p>•	Responsive web design is less expensive than a mobile app. So the cost can be one of the disadvantages.</p>
<p>•	It uses a single codebase. The use of a single codebase can make development faster.</p>
<p>•	Maintenance will be easier over time when compared to the mobile app.</p>
<p><b>The disadvantage of responsive web design</b></p>
<p>•	Nowadays most people are using mobile devices. So responsive isn’t as fast or as functional as native mobile apps. It takes a long time to navigate to a mobile site through a phone’s browser when compared with the convenience of an app, and once users are there, the platform usually performs slower.</p>
<p><b>Advantage of mobile App</b></p>
<p>•	Mobile Apps have greater functionality than mobile-oriented sites. It integrates can with other features and tools easily.</p>  
<p>•	Mobile Apps usually have faster loading times than web pages as they aren’t cluttered with unnecessary site elements. The user experience is optimized.</p>
<p>•	It is better for brand awareness and marketing. </p>
<p>Therefore, both have their own pros and cons as summarized above. I think the owner will decide based on the their primary goal and budget at hand.  If the company is capable to spend money mobile app can be the first choice.  Otherwise still responsive site also works reasonably.</p>






